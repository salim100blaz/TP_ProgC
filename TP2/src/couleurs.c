#include <stdio.h>
#include <string.h>


struct Couleur {
    unsigned char r, g, b, a;
};

int main() {
    struct Couleur c[10] = {
        {0xef,0x78,0x12,0xff},
        {0x2c,0xc8,0x64,0xff},
        {0x00,0x11,0x22,0x33},
        {0xaa,0xbb,0xcc,0xdd},
        {0x10,0x20,0x30,0x40},
        {0x55,0x66,0x77,0x88},
        {0x90,0x80,0x70,0x60},
        {0xff,0x00,0xaa,0x55},
        {0x12,0x34,0x56,0x78},
        {0xde,0xad,0xbe,0xef}
    };

    for (int i = 0; i < 10; i++) {
        printf("%d %d %d %d\n", c[i].r, c[i].g, c[i].b, c[i].a);
    }

    return 0;
}
